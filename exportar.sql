--------------------------------------------------------

  CREATE OR REPLACE FORCE VIEW "PAP_RENOVARE"."ANIMAIS" ("ID_ANIMAL", "NOME", "ESPECE", "SEXO", "IDADE", "RACA") AS 
  select
animal.id_animal,
animal.nome,
especie.espece,
sexo_animal.sexo,
idade_animal.idade,
raca_animal.raca
from animal
inner join sexo_animal on sexo_animal.id_animal = animal.id_animal
inner join raca_animal on raca_animal.id_animal = animal.id_animal
inner join idade_animal on idade_animal.id_animal = animal.id_animal
inner join especie on especie.id_animal = animal.id_animal

;
